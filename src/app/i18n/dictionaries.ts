type EnglishDictionary = typeof dictionaries.en;

export type TranslationKey = keyof EnglishDictionary;
export type TranslationValues = Record<string, number | string>;

export function normalizeLocale(locale?: string | null): LocaleCode {
  if (!locale) {
    return defaultLocale;
  }

  const normalized = locale.toLowerCase().split(/[-_]/)[0];

  return isSupportedLocale(normalized) ? normalized : defaultLocale;
}

export function isSupportedLocale(locale: string): locale is LocaleCode {
  return supportedLocales.includes(locale as LocaleCode);
}

export function translate(
  locale: LocaleCode,
  key: TranslationKey,
  values: TranslationValues = {},
) {
  const dictionary = dictionaries[locale] as Partial<EnglishDictionary>;
  const template = dictionary[key] ?? dictionaries.en[key] ?? key;

  return template.replace(/\{(\w+)\}/g, (_, name: string) =>
    values[name] === undefined ? `{${name}}` : String(values[name]),
  );
}
